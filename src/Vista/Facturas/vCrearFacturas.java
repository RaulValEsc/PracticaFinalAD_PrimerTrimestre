    private void bCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCrearActionPerformed
        if(!etNumFactura.getText().isEmpty()&&!etDni.getText().isEmpty()&&!etFecha.getText().isEmpty()){
            cabecera = new FacturasCab();
            for(Clientes c : Principal.listaClientes){
                if(c.getDnicif().equals(etDni.getText())){
                    cabecera.setClientes(c);
                    break;
                }
            }
            cabecera.setNumfac(Integer.parseInt(etNumFactura.getText()));
            Date fecha = new Date();
            SimpleDateFormat format = new SimpleDateFormat("dd-MM-yyyy");
            try {
                fecha = format.parse(etFecha.getText());
            } catch (ParseException ex) {
                System.out.println("Error n el parseo de la fecha");
            }
            cabecera.setFechafac(fecha);
            vCrearLineasFactura v = new vCrearLineasFactura(null, true);
            this.setVisible(false);
            v.setVisible(true);
            dispose();
        }else{
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "No puede haber un campo vac√≠o", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bCrearActionPerformed

